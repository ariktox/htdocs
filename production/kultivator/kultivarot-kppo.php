<p>Залогом надёжности культиватора есть его рама, состоящая из трёх секций – центральной и двух крыльев. Все несущие элементы рамы выполнены из прочных профильных труб высокой жесткости. Крылья шарнирно соединены с центральной рамой и переводятся в транспортное положение двумя гидроцилиндрами, обеспечивающими поворот крыльев на 110?. При этом центра тяжести крыльев смещаются к центру тяжести средней рамы, что при достаточно широкой колёсной базе определяет высокую устойчивость культиватора при переездах по пересечённой местности.</p>
