				<h1 class="title">Культиватор прицепной предпосевной обработке КППО-4 </h1>
				<div class="entry">
                <main>	
                <img src="../../images/kultivator-kppo/kultivator_kppo-4.jpg" width="620" alt="Культиватор КППО-4 с катком" title="Культиватор КППО-4 с катком" style="float:left" /> 				
<p><strong>Культиватор прицепной предпосевной обработке почвы КППО-4 </strong> предназначен для сплошной культивации почвы, а также для предпосевной обработки. Может работать по необработанной стерне. Конструкция рамы и рабочих органов позволяют культиватору очень качественно и равномерно обрабатывать, и выравнивать верхний слой почвы на глубину 5-15 см. Особо надо отметить эффективное противоэрозионное воздействие агрегата на обрабатываемую поверхность поля. Для наиболее лучшего выравнивания поля и измельчения крупных комков культиватор оснащен 2-х рядной финишной пружинной бороной. Диаметр зуба пружинной бороны составляет 12 мм. Для более мелкого дробления комьев земли и выравнивания, за пружинной бороной устанавливается каток (дополнительная опция). Конструкция культиватора позволяет увеличить ресурс работы деталей культиватора. Защитные механизмы препятствуют разрыву рамы и повреждению рабочих органов при столкновении в земле с камнями или плитняком. Культиватор КППО выполняет сразу несколько операций за один проход, позволяя тем самым добиться экономии горюче-смазочных материалов, а также амортизации и рабочего времени трактора. Рама на двух широко расставленных колёсах позволяет отлично копировать рельеф поля, избегая пропусков при культивации. Культиватор приспособлен для работы на высокой рабочей скорости (до 13 км/ч), что позволяет добиться большей производительности. Рекомендованная мощность трактора 20-22 л.с. на 1 метр ширины захвата культиватора. </p>
