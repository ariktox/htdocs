<p>Культиватор выполнен по полуприцепной схеме и агрегатируется с трактором с помощью регулируемого по высоте длинного тягово-сцепного устройства высокой жесткости, что позволяет минимизировать влияние смещения точки прицепа при движении трактора, а также существенно повышает маневренность агрегата «трактор-культиватор» при разворотах.</p>
<p>Культиваторы с шириной захвата до 8,4 метра включительно оборудованы четырьмя  одноосными колёсными механизмами, с шириной захвата свыше 10 метров – четырьмя механизмами, оснащенными двухколёсными балансирными тележками с межосевой базой 700 мм, что обеспечивает максимальную устойчивость культиватора при работе и точную обработку на заданную глубину.</p>
<p>Рабочий клиренс культиватора (от низа рамы до подошвы лапы) составляет 710 мм, что совместно с  4-х рядной (ширина захвата до 8,4 метра включительно) или 5-ти рядной ( ширина захвата свыше 10 метров) расстановкой рабочих органов с наименьшим расстоянием между соседними в ряду  690 мм полностью исключает забивание подрамного пространства пожнивными и растительными остатками. А при межцентровом расстоянии 230 мм между соседними  по ходу плоскорезными лапами шириной захвата 310 мм (взаимное перекрытие 80 мм) обеспечена 100%  подрезаемость любых сорняков. </p>
<p>Значительное ( более 120 кг на носке лапы) усилие срабатывания предохранительной пружины позволяет обрабатывать почву очень высокой твёрдости на строго заданную глубину. </p>
<p>Финишная обработка обеспечивает трёх рядная пружинно-<a href="../zapchasti-kultivator/zubovaya-borona.html">зубовая борона</a> и прикатывающим  полосчатым катком  255 с регулируемым усилием нажатия и шагом следа полос 100мм.</p>
<p>Диапазон глубины обработки регулируется плавно в пределах от 0 см до 17 см (и глубже).</p>
<p>Регулировка глубины хода культиватора обеспечивается 4-мя винтовыми стяжками,изменяющими угол наклона гидрофицированных  колёсных механизмов. При этом при работе гидроцилиндры полностью закрыты, при транспортировании – полностью открыты. Независимо от регулировки глубины транспортный клиренс будет больше, чем 300 мм. Доступ к элементам регулирования удобен, так как все точки расположены в2озле переднего бруса культиватора.</p>
<p>Секционное устройство рамы позволяет крыльям перемещаться на 5?7? вверх и вниз, что делает возможным качественную обработку склонов, понижений и холмов, точно копируя рельеф.</p></main>
   
