            <h3>Культиватор КРН (КМН)  выпускается в следующих модификациях:</h3>
            <br>
            <p>- КМН-5.6 (культиватор КРН) –  навесной междурядный, шириной захвата 5,6м для обработки междурядий 700мм,  9 секций рабочих органов;<br>
              - КМН-5.6-01(культиватор  КРН)  – навесной междурядный, шириной  захвата 5,6м для обработки 8-и междурядий 700 мм одновременно и  внесения удобрений , 9 секций рабочих органов, комплектуется туковысевающим  аппаратом на базе АТП-2 (по заказам потребителей).<br>
              - КМН -5.4 (культиватор  КРН)  – навесной междурядный, шириной  захвата 5.4 м  для обработки 12-и междурядий 450   мм, 13 секций рабочих органов;<br>
              -КМН-5.4 -01(культиватор  КРН)   – навесной междурядный, шириной  захвата 5.4 м  для обработки 12-и  междурядий и  одновременно внесения удобрений , 13 секций рабочих органов, комплектуется  туковысевающим аппаратом на базе АТП -2 ( по заказам потребителей).</p>
            <hr>
            <h3>Особенности конструкции  секции рабочих органов</h3>
            <br>
            <p>В шарнирах параллелограммной  подвески грядиля, обеспечивающей копирование почвы при обработке установлены  двухсто-ронне закрытые шарикоподшипники, что значительно улучшает  эксплуатационные качества культиватора (рис):<br>
              - ввиду отсутствия трения  скольжения резко снижается усилие вертикального перемещения грядиля, что  значительно улучшает копирование почвы;<br>
              - разнесенные подшипниковые  опоры обеспечивают горизонтальную устойчивость грядиля, благодаря чему нет  опасности по-вреждения, как защитной зоны, так и посева;<br>
              - ввиду отсутствия зазоров в  шарнирах невозможно попадание туда инородных тел и грязи, что может вызвать  заклинивание грядиля с рабочими органами и, как результат, некачественную  обработку почвы;<br>
              - не требуется смазка  шарниров, так как в подшипники заложена смазка на весь ресурс эксплуатации.</p>
            <p>Транспортное  устройство служит для транспортировки культиватора по дорогам общего  назначения. Позволяет агрегати-ровать культиватор торцом с трактором и при  соответствующей переустановке колес транспортировать его в полунавесном  варианте.</p>
