            <p>Культиватор КМН  (функциональный аналог культиватора КРН) предназначен для междурядной обработки  восьми, а так же двенадцати рядных посевов высокостебельных пропашных культур,  высеянных с междурядьями 450 –700мм и более. Во всех почвенно-климатических  зонах. <br>
              Рабочая скорость обработки – до  10км/час, что обеспечивает производительность за один час обработки  соответственно 6,0/5,8  и 4,5 гектара, исключая  районы с каменистыми почвами.</p>
