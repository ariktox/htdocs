            <p>Культиватор КРН (КМН) в зависимости от установленных рабочих органов и приспособлений и схемы их установки может выполнить следующие операции:<br />
              1) Подрезание сорной растительности и рыхление почвы в междурядиях;<br />
              2) Нарезку поливных борозд;<br />
              3) Окучивание растений;<br />
              4) Обработку защитных зон рядков методом присыпания сорняков;</p>
            <div style="float:right; width:200px; margin:0 5px;"> <img src="../../images/cultivator_kmn/Sekcia-KRN-bez-rabochix-organov-small.jpg" alt="Секция КРН без рабочих органов" title="Секция КРН без рабочих органов"> <strong>Секция КРН без рабочих органов</strong> </div>
            <p>5) Подкормку растений минеральными удобрениями при установке туковысевающих аппаратов типа АТП-2;<br />
              6) Предпосевную обработку почвы;<br />
              7) Боронование междурядий (при установке соответствующих рабочих органов).</p>
            <p>Культиватор КРН с секциями на  подшипниковых узлах навесного исполнения.  Соединение культиватора с трактором осуществляется посредством замка,  соединенного с рамой и автоматической сцепкой СА-1, сагрегатированной с  механизмом навески трактора.<br>
              Культиватор КРН (КМН)  агрегатируется с тракторами тягового класса  1,4-2,0 тонны (МТЗ 50/52; МТЗ 80/82;   ЮМ36А/6АМ).</p>
