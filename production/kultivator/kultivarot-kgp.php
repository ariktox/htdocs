                <h1 class="title">Культиватор грунтообрабатывающий прицепной - КГП</h1>
                <hr />
                <div class="entry">
                    <main>
                    <img src="../../images/cultivator-kgp/kgp-4.jpg" width="620" alt="Культиватор КГП-4 с катком" title="Культиватор КГП-4 с катком" />
                    <p>Предназначено для первичной обработки стерни, подготовки посевного горизонта после вспашки или боронования,
                        весеннего рыхление, улучшающее вентилирование почвы для ускорения высыхания, культивации
                        для подавления сорняков после применения тяжелых или противоэрозионных культиваторов а так же рыхление мерзлой
                        почвы для внесения органических удобрений.</p>
                        	<div style="float:left; width:350px; margin: 0 10px 20px 0; box-shadow:0 3px 5px #CCC;"><img src="../../images/cultivator-kgp/Stoika_kultivatora_KGP.jpg" alt="стойка КГП" title="Детализация стойки КГП" width="350"></div>
                        <p>Основное достоенство. Лапы со стойкой и пружинная подвеска создают при работе
                        культиватора микроколебания, способствующие крошению комьев по естественным
                        границам фракций.</p>
                        <div style="margin: 20px; padding:10px; float:right; width:200px; box-shadow:0 3px 5px #CCC;">
<p>Лапа КГШ (КГП) так же подходит для культиваторов<br>John Deere<br>Case<br> и Флексикоил</p>
	<img src="../../images/cultivator-kgsh/stoika_kgsh/Lapa-KGSH.jpg" width="200" alt="Лапа КГШ, John deere, case" title="Лапа КГШ, John deere, case">
<p>1) Ширина 305 мм.<br>
2) Марка стали - 65 Г.<br>
3) Толщира - 8 мм.</p>
</div>
<p>Культиваторы КГП выпускаются с шириной захвата 4 / 6,5 / 7 / 7,4 / 7,8 / 8,4 / 9,3 / 10,4 / 11 / 12 метров.
<div style="float:left; width:280px; margin: 0 10px 20px 0; padding:10px; box-shadow:0 3px 5px #CCC;"><img src="../../images/cultivator-kgp/Borona-zubchataya.jpg" style="float:left;" alt="Борона зубчатая" title="Зубчатая борона КГП" width="150"><p>Финишная обработка обеспечивается 3-х рядной пружинно-зубовой бороной и прикатывающим пластинчатым катком.</p></div>

