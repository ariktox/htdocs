<p>Подъем рабочих органов прицепного культиватора  КСГП-4; КСГН-4  и приспособления с навешенными  боронами в транспортное положение осуществляется при помощи гидроцилиндра, установленного к центральной тяге.</p>
<p>Культиватор КСГП-4; КСГН-4 агрегатируется:<br />
 - один  культиватор в прицепном и навесном   вариантах с тракторами класса 1.4 – 2т., например МТ3 50/52; МТ3 80/82; <br />
 - два культиватора в прицепном  варианте при помощи  гидрофицированной  сцепки /СП-11/ с тракторами класса 3т, напри-мер Т –150; Т –150К;<br />
 - три или четыре культиватора  в прицепном  варианте при помощи прицепной универсальной гидрофицированной  сцепки /СП-16/ с тракторами класса 4 – 6 т, например Т –4; К-700; К – 701 ; Т – 100М. </p>
<p>Культиваторы, работающие в агрегате с трактором и сцепкой, располагаются в шеренгу, для  соединения культиваторов меж-ду собой необходимы соединительные шарниры – при четырех культиваторах – 3шт., при трех –2шт. и при двух – 1шт.</p>
<p>Приспособление для навески борон обеспечивает навеску на  культиватор КСГП-4; КСГН-4 звеньев зубовых борон БЗСС-1,0 в количестве 4 штук или пружинных борон. При работе навесных модификаций культиваторов со средними зубовыми боронами необходимо снять имеющиеся грузы задних колес трактора и поставить на передние. Грузы прилагаемые к трактору, поставить на передний брус рамы. Эти перестановки сохраняют продольную устойчивость трактора.</p>
<p>Культиватор отгружается предприятием изготовителем в собранном виде за исключением установки рабочих органов.</p>
