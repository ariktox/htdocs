				<h1 class="title">Культиватор Сплошной Обработки Почвы - КСГП-КСГН</h1>
                <hr />
				<div class="entry">
                <main>	
                <img src="../../images/cultivator_ksgp/cultivator-ksgp-4.jpg" width="400" alt="культиватор междурядной обработки" style="float:left; margin:10px;" />			
<p>Культиватор КСГП-4; КСГН-4 унифицированный в прицепном и навесном вариантах с приспособлением для навески борон предназначен для предпосевной обработки почвы и обработки па-ров с одновременным боронованием на рабочей скорости до 12км/ч во всех почвенноклиматических зонах, исключая районы с каменистыми почвами и стерневыми фонами.</p>
<p>Культивация поля производится поперек или под углом к направлению вспашки, а повторные обработки – поперек  направления предшествующих  культиваций.</p>
