<p><strong>Агрегатный многофункциональный культиватор АБК-7</strong> предназначен  для основной и предпосевной обработки почвы под зерновые, кормовые и технические культуры. Благодаря установленной дисковой батареи <strong>культиватор</strong> эффективно выполняет работы на полях с большим количеством растительных остатков. За один проход <strong>культиватор</strong> позволяет измельчить, перемешать и заделать растительные остатки, полностью уничтожить сорняки, выполнить культивацию на строго заданную глубину, разрыхлить и достаточно уплотнить почву во избежание потерь влаги, полностью подготовив почву под последующие агротехнические операции.
Культиватор АБК-7 агрегатируется с трактором тягового класса 40 кН и мощностью не менее 210 л.с.
</p>
