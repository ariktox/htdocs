<p>АБК предназначен для дискования и сплошной культивации, для предпосевной обработки, а также культивации паровых и стерневых участков. Конструкция рамы и рабочих органов культиватора позволяют очень качественно и равномерно обрабатывать и выравнивать слой земли на глубине от 5 до 15 см. </p>
<p>Конструкция культиватора позволяет значительно увеличить ресурс работы деталей и узлов, что крайне важно при оценке стоимости сервисных работ и ремонта. Культиватор АБК выполняет максимальное количество операций за один проход, позволяя тем самым добиться экономии горюче-смазочных материалов, а также экономии амортизации и времени работы трактора. Трехсекционная рама плавающего типа и тандемная подвеска колес позволяют отлично копировать рельеф поля, избегая пропусков. Фронтальные диски мульчируют растительные остатки. Конструкция дисков предусматривает различные регулировки угла атаки и глубины, а также защиту от механических повреждений. Широкая растановка лап и увеличенный клиренс подрамного пространства полностью исключают забивание грязью с пожнивными остатками. Безостановочные пружинные механизмы препятствуют разрывам рамы и повреждению стоек и стрельчатых лап при столкновении в земле с камнями или плитняком. </p>
<p>Для наиболее качественного выравнивания поля и измельчения комков земли культиватор комплектуется пружинной бороной и катком. Сечение граблин пружинной бороны составляет 12 мм. Каток имеет амортизирующую систему. Культиватор разработан с возможностью работы на высокой рабочей скорости (до 13 км/ч), что позволяет добиться большей производительности. Культиватор производится с различной шириной захвата от 4 до 12 метров. Рекомендованная мощность трактора 30-32 л.с. на 1 метр ширины захвата культиватора.</p>
<p>	Культиватор  полностью отвечает конструкторской документации, отраслевым и государственным стандартам. Гарантируется исправность культиватора на протяжении 12 месяцев со дня ввода в эксплуатацию, при условии соблюдения потребителем технических требований эксплуатации культиватора. Дата ввода в эксплуатацию подтверждается подписью руководителя предприятия-производителя. Производитель оставляет за собой право вносить модификационные изменения в конструкцию культиватора.</p>
