                <h1 class="title">Глубокорыхлитель РН - Навесной</h1>
                <div class="entry">
                    <main>
                        <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Глубокорыхлитель РН предназначен для применения в разных агроклиматических зонах, на любых видах почв, в том числе на участках подверженных водной и ветровой эрозии.</p> <br />
                       
                        <img src="../../images/glubokorixlitel_rn/rn_5_36/glubokorixlitel_rn_5.jpg" alt="Глубокорыхлитель навесной РН-5-3,6" title="Глубокорыхлитель навесной РН-5-3,6" width="620" />
                        <br>
                        <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Глубокорыхлители данного типа могут быть использованы на почвах с влажностью до 30%, удельным сопротивлением до 1,2 килограммов на квадратный сантиметр и твёрдостью 40 килограммов на квадратный сантиметр, за исключением почв с крупными каменистыми включениями. Глубокорыхлители РН созданы для работы в сложных климатических условиях.</p><hr />
