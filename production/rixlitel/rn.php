<p>Рабочий орган</p>
<p>Параболическая стойка АКГР-РН изготовтены из полосы 40х80 (60С2А)</p>
<p>Менее подвержено износу в процесе работы за счёт использование разных наконечников с различной геометрией.</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Данные глубокорыхлительные агрегаты предназначены для разрушения плужной подошвы, безотвального глубокого рыхления,  углубления пахотного горизонта, глубокого рыхления паровых полей и  почвы на глубину от 25 до 55 сантиметров, с целью аэрации почвы,  улучшения циркуляции влаги и полезных микроэлементов между верхнем (до 25 см.) и нижними слоями (от 55 см.) почвы,  параллельно с которым производится частичное крошения почвы. <br />

&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Глубокорыхлители РН имеют от 3 до 5 рыхлительных стоек параболической формы. </p>
