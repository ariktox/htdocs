                    	<p>&nbsp; &nbsp;&nbsp;&nbsp;Главное назначение АКГР - разрушение «плужной подошвы»- уплотненного в результате многократного механического воздействия слоя почвы, расположенного ниже горизонта регулярной обработки. В результате такого уплотнения нарушается естественный газо и водообмен в почве, разрушаются капиллярные каналы,  по которым избыток влаги  в верхних слоях перемещается и накапливается в нижних слоях, а затем восполняет недостаток во влаге и растворенных в ней газах, минеральных и органических питательных веществах, поднимаясь вверх, к корневой системе растений.
