<p>Рабочий орган</p>
<p>Параболическая стойка АКГР-РН изготовтены из полосы 40х80 (60С2А)</p>
<p>Менее подвержено износу в процесе работы за счёт использование разных наконечников с различной геометрией.</p>
<div style="margin: 10px; padding:10px; float:right; width:290px; box-shadow:0 3px 5px #CCC;">
<p>Стойка чизельная</p>
	<img src="../../images/glubokorixlitel_rn/chizelnaya_stoika.jpg" width="290" alt="Стойка чизельная" title="Стойка чизельная">
</div>
<p>Рекордно высокие температуры, продолжительное отсутствие осадков (осень 2011- в среднем по Украине 90 дней без осадков, в отдельных регионах до 5 месяцев в период осенних полевых работ), вынуждает по новому подходить к землепользованию, искать пути к накоплению и сохранению влаги, к естественному восстановлению почвы, регенерации ее гумусообразующих структур.</p>
<p>Дисковый глубокорыхлитель АКГР, как агрегат, объединил в себе:<br />
- дисковую фронтальную борону, измельчающую растительные остатки и заделывающую их равномерно в обработанный слой почвы глубиной 15-17 см, создавая мульчирующий эффект;<br />
- глубокорыхлитель, обеспечивающий безотвальную обработку почвы на глубину до 45 см без выноса нижнего слоя.<br />
- чизельная секция, закрывающая след глубокорыхлительной лапы со стойкой, перемешивающая  выравнивающая слой мульчи.<br />
- пластинчатый каток, который дополнительно перемешивает слой мульчи, крошит груды и уплотняет верхний слой, предотвращая потерю влаги.<br /></p>
<p>     Дисковая секция состоит из двух фронтальных дисковых встречно-направленных дисковых рабочих корпусов. Каждая батарея имеет индивидуальную настройку глубины рабочего хода посредствам винтовых талрепов. Бесступенчатая регулировка позволяет вести обработку на глубине от 0 до 200 мм (в зависимости от твердости и  уплотненности почвы).   </p>  
<p>      Угол атаки к линии тяги рабочих вырезных дисков не регулируется и составляет 18.Угол вертикального наклона диска  изменяется от 0? до5?-7? в зависимости от регулировки по высоте положения дисковой батареи. Допускается продольное перемещение дисковых корпусов вдоль несущего бруса.</p>
<p>    Секция  глубокорыхлителей представляет собой прочную несущую балку, закрепляемую на центральной раме за опорными колесами агрегата, с узлами крепления параболических стоек с долотообразными лапами. Стойки лап крепятся в кронштейнах балки двумя болтами: высокопрочным основным и термически обработанным срезным предохранительным, обеспечивающим разворот стойки с выходом из почвы, при столкновении лапы с твердым препятствием. Термически обработанная, с твердосплавной наплавкой долотообразная лапа с развитой рабочей поверхностью, установленная на прочной, термообработанной параболической пружинной стойке легко внедряется в почву на заданную глубину ниже горизонта плужной подошвы, взрывает подошву по линии своего движения боковыми «крыльями» приподнимает уплотненный слой, сдвигает его в стороны, подошва растрескивается, теряя свою монолитность. При этом образуется сетка трещин, пронизывающая слой почвы от глубины обработки до поверхности, посредством которой и восстанавливается естественные процессы газо- и водообмена,обеспечивающие оптимальное протекание химических и биологических процессов в почве. Рабочие органы глубокорыхлительной секции расположены в один ряд, с расстоянием между соединениями 720мм. Соответственно,3х стоечный агрегат имеет ширину захвата 2160мм,4х стоечный-2880мм, и 5и стоечный-3600мм.
Чизельная  секция состоит из комплектных рабочих органов, установленных на центральной раме  на кронштейнах, позволяющих регулировать рабочую глубину.</p>
<p>Рабочий корпус состоит из узла присоединения, держателя пружинной стойки, возвратной пружины, пружинной стойки с оборачиваемой рыхлительной лапой. Чизельные  рабочие органы установлены сзади и равноудалено от глубокорыхлителей.</p>
<p>Каток представляет собой полый цилиндр, диаметром  400мм. образуемый 9-ью полосами, свободно вращающийся в подшипниковых узлах. Крепление и винтовые талрепы позволяют регулировать положение катка по вертикали в широком диапазоне.</p>
