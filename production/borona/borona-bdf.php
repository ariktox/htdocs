            <div style="float:left; width:400px; margin: 0 10px 20px 0; box-shadow:0 3px 5px #CCC;"><img src="../../images/borona-navesnaya-bdf/stoika_bdf_bdfp.jpg" alt="стойка бороны БДФ" title="Детализация стойки бороны БДФ" width="400"></div>
            <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Дисковый рабочий орган, представляющий собой установленный на ступице вырезной сферический <a href="../zapchasti-boroni.html">диск (ромашка), диаметром 650 мм</a>. и толщиной 6 мм., с внутренней заточкой по всему контуру. Благодаря особой геометрии трубчатой стойки установлен под оптимальным углом атаки к линии тяги и углом к вертикали, обеспечивающим качественное крошение, дробление и оборот срезаемого пласта. </p>
            <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Борона производится как навесная БДФ (рабочая ширина от 1,8 до 3,1 метров), так и <a href="borona-bdfp.html">прицепная БДФП</a> (рабочая ширина от 2,4 до 7,0 метром). В обоих случаях борона комплектуется пластинчатыми цилиндрическими катками диаметром O 400, а бороны с гидрофицированным механизмом подъема крыльев (ширина захвата от 5,5 метра и выше) комплектуются набором штанг, позволяющих устанавливать комплектацию из двух катков диаметром O 400 и O255 мм. или из трёх рядной пружинно-зубовой бороны и катка O255 мм.</p>
            <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Борона содержит два ряда дисков, каждый из которых расположен на индивидуальной стойке, которая крепится к брусу двумя Г-образными скобами (диаметр 20 мм.). Данная конструкция исключает забивание междискового пространства. Борона может эксплуатироваться во всех почвенно-климатических зонах где влажностью почвы не прывешает 27 процентов и с твердостью почвы не больше 3,5 Мпа, а так же не рекомендуется эксплуатация данной бороны в районах со значительными каменистыми включениями. За счёт использования в конструкции  индивидуальных стоек фронтальная борона может использоваться на высоких рабочих скоростях – до 14 км/ч.</p>
            <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;В отличии от подобных борон других производителей конструкции данной бороны используются диски ромашка (вырезные сферические) диаметром O 650 мм, толщиной 6мм, изготовленные из стали марки 65Г. Во время изготовления диски термически обрабатываются, что обеспечивает твёрдость 38-46 HRC.</p>
            <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Диски устанавливаются на ступицы, которые вращются вокруг оси на роликовых подшипниковых узлах под углом атаки 21 градус и вертикальным наклоном 21 градус (конструкция запатентована частным предприятием “Агротехкомплект”).</p>
            <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Металлический пыльник который установлен на ось при сборке перекрывает ступицу, образуя защитную конструкцию, которая дополняется манжетой с сальником внутри и пыльником снаружи. Узел очень хорошо герметизирован от внешней среды за счёт данной конструкции.</p>
            <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Фиксирующая и регулирующая гайка подшипника ступицы закрывается фланцем с колпаком, который выполняет две функции : </p>
            <ul>
              <li>герметизацию ступицы</li>
              <li>закрепление рабочего органа (диск БДФ) на ступице. </li>
            </ul>
            <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Чистик двумя болтами с квадратным подголовниками  крепится к кронштейну с двумя пазами, который в свою очередь приварен к стойке и при работе бороны очищает рабочий орган (диск БДФ ромашка) от налипания почвы, наматывание растений, забивания пожнивных остатков, а так же направляет поток почвы в сторону дробя и оборачивая его.</p>
            <p>Задние катки бороны обеспечивают выполнение двух функций:</p>
            <ul>
              <li>- дробят почву</li>
              <li>- уплотняет и выравнивает поверхность</li>
            </ul>
            <p>Кроме выше упомянутых функций с помощью катка для навесных борон выставляется глубина обработки почвы, которая варьируется, как указанно выше от 8 до 18 см.</p>
            <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Необходимая глубина рыхления и стабильный ход агрегата борона-трактор обеспечивается талрепом (винтовой  механизм с  правой и левой резьбой), который обеспечивает регулировку вертикального положение катка .</p>
            <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Борона агрегатируется с оснащенными гидросистемой тракторами мощностью от 40 (для навесной бороны БДФ-1,8) до 320 (для <a href="borona-bdfp.html">прицепной бороны</a> БДФП-7,0) лошадиных сил в зависимости от ширины захвата орудия. </p>
            <p>При помощи талрепов производится регулировка высоты хода (глубины обработки) пластинчатого катка диаметром 400 мм.</p>
            <p>Конструкция крепления катка позволяет смещать (передвегать) каток в лево или право (при необходимости).</p>
