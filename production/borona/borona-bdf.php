        <h1 class="title">Борона БДФ - Навесная - Дисковая </h1>
        <div class="blue_back">
          <h3>Борона  дисковая с фронтальным расположением рабочих органов</h3>
        </div>
        <hr />
        <div class="entry">
          <main> <img src="../../images/borona-navesnaya-bdf/bdf-2.1/borona_bdf_21_vid1.jpg" alt="Борона навесная БДФ-2.1" title="Дисковая навесная борона БДФ-2.1" width="480" style="float:left;" />
          
            <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Борона предназначена для сплошной и предпосевной обработки  почв различного механического состава под технические, зерновые и кормовые культуры и с измельчением и  заделкой в почву  растительных остатков (освежения задернелых лугов) в различных агротехнических схемах. Регулируемая глубина обработки составляет примерно от 8 до 18 см. </p>
            <div style="clear:both;"></div>
