<br>
                        <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Данный  лущильник обеспечивает глубину обработки от 4 до 12 сантиметров и рабочую скорость обработки 12 км./ч. Лущильник оснащён двумя рядами сферически вырезных дисков (в народе ромашка), толщиной 6 мм., диаметром 485 мм., из стали марки 65 Г. </p>
                        <div style="float:left; width:320px; margin: 0 10px 20px 0; box-shadow:0 3px 5px #CCC;"><img src="../../images/lushilnik-pricepnoi-ldfp/LDFP-uzel-2.jpg" alt="Узел ЛДФП-2" title="Узел ЛДФП-2" width="320"></div>
                        <p>Во время изготовления диски термически обрабатываются, что обеспечивает твёрдость 38-46 HRC.</p>
                        
                        <div style="float:left; width:400px; margin: 0 10px 20px 0; box-shadow:0 3px 5px #CCC;"><img src="../../images/lushilnik-pricepnoi-ldfp/ldfp_uzel_1.jpg" alt="Узел ЛДФП-1" title="Узел ЛДФП-1" width="400"></div><p>Крылья с установленными рабочими органами благодаря системе шарниров и регулировочных упоров копируют рельеф обрабатываемой почвы, обеспечивая качество и равномерность обработки по всей ширине захвата.</p>
