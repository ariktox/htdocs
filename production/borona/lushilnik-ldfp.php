                      <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;На данном лущильнике используются, уже зарекомендовавшие себя с положительной стороны подшипниковые узлы дискового рабочего органа бороны БДФ – БДФП (конструкция запатентована частным предприятием “Агротехкомплект”),  рассчитанные для работы с дисками диаметром 650 мм., что обеспечивает многократный запас прочности. В подшипниковых узлах, установленных на пружинные стойки из термообработанной высокопрочной рессорной стали марки 60С2А с 3d зашитой. используются два конических роликовых подшипника 127509 (с увеличенной грузоподъемностью), Сзади лущильник укомплектован  пластинчатым катком диаметром 400 мм. Регулировка высоты хода катка (глубины хода) производится при помощи талрепов.</p>
