                                <td>8-18 см.</td>
                            </tr>
                            <tr>
                                <td>Высота</td>
                                <td>1400 см.</td>
                            </tr>
                            <tr class="bg_col_brown">
                                <td>Транспортная скорость, не больше</td>
                                <td>20 км/час</td>
                            </tr>
                            <tr >
                                <td>Дорожный просвет,  не меньше</td>
                                <td>300 мм,</td>
                            </tr>
                            <tr class="bg_col_brown">
                                <td>Перевод в транспортное и рабочее<br />
                                    положения</td>
                                <td>Гидрофицированным колесным, <br />
                                    механизмом транспортировки дисковой борони,<br />
                                    соединением с гидросистемой трактора</td>
                            </tr>
